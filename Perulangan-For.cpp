#include <iostream>
using namespace std;

int main()
{

	// for
	for (int a = 1; a <= 10; a++)
	{
		cout << a << endl;
	}

	cout << endl;
	cout << endl;

	//
	for (int a = 1; a <= 10; a += 2)
	{
		cout << a << endl;
	}

	cout << endl;
	cout << endl;

	//
	for (int a = 10; a >= 1; a -= 2)
	{
		cout << a << endl;
	}
}